public class ForLoop {

    public static void main(String[] args) {
//
        //  (inicjalizacja licznika/ów; warunek dalszego wykonywania; zmiana danych licznika/ów)
        for (int counter = 1; counter <= 10; counter++) {
            System.out.println("licznik: " + counter);
        }
        // 1. inicjalizacja licznika - wykonywane tylko raz!
        // 2. sprawdzenie warunku
        //    2.1 jeśli true -> przejdź do 3.
        //    2.2 jeśli false -> zakończ pętle
        // 3. wykonaj blok kodu
        // 4. wykonaj operację zmiany licznika/ów
        // 5. wróć do 2.


    }
}
